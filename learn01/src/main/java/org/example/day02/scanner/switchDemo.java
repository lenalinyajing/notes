package org.example.day02.scanner;

public class switchDemo {
    public static void main(String[] args) {
        String name = "lin";

        switch (name){
            case "lll":
                System.out.println("lll");
                break;
            case "yyy":
                System.out.println("yyy");
                break;
            default:
                System.out.println("yes");
        }
    }
}
